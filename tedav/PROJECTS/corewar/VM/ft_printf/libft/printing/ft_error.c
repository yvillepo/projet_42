#include "libft.h"

void		ft_error(char *msg)
{
	ft_putstr_fd("\033[31m", 2);
	ft_putstr_fd(msg, 2);
	ft_putchar_fd('\n', 2);
	ft_putstr_fd("\033[0m", 2);
	exit(0);
}
