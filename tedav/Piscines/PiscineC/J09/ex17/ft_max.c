int		ft_max(int *tab, int length)
{
	int		max;
	int		i;

	i = 1;
	if (tab[0])
		max = tab[0];
	while (i < length)
	{
		if (tab[i] > max)
			max = tab[i];
		i++;
	}
	return (max);
}
