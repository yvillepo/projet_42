int		match(char *s1, char *s2)
{
	while (*s2 != '\0')
	{
		if (*s1 != *s2 && *s2 != '*')
			return (0);
		if (*s2 == '*')
		{
			if (*(s2 + 1) == '*')
			{
				s2++;
				continue;
			}
			if (*(s2 + 1) != *s1)
			{
				s1++;
				continue;
			}
		}
		else if (*s1 == *s2)
			s1++;
		s2++;
	}
	if (*s1 == '\0' && *s1 == *s2)
		return (1);
	return (0);
}
