char	*ft_strcpy(char *s1, char *s2)
{
	int		n;

	n = 0;
	while (n < 1000000)
	{
		*s1[n] = *s2[n];
		if (*s2[n] == '\0')
		{
			return (s1);
		}
		n++;
	}
}
